package IntermediateLevel5;

public class Shuriken {
    private String name;

    public Shuriken() {

    }

    public Shuriken(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName() {
        this.name = name;
    }

    public String toString() {
        return "Shuriken: " + name;
    }
}